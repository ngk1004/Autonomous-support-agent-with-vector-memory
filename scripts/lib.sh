source "$SCRIPT_DIR/art.sh"

open_url() {
  local url="$1"
  if command -v open >/dev/null 2>&1; then
    open "$url" 2>/dev/null || true
  elif command -v xdg-open >/dev/null 2>&1; then
    xdg-open "$url" 2>/dev/null || true
  else
    say "Open in browser: $url"
  fi
}

load_env() {
  if [ -f "$PROJECT_DIR/.env" ]; then
    # shellcheck disable=SC1091
    set -a && source "$PROJECT_DIR/.env" && set +a
    return 0
  fi
  return 1
}

check_docker() {
  command -v docker >/dev/null 2>&1 && docker info >/dev/null 2>&1
}

check_n8n() {
  curl -sf http://localhost:5678/healthz >/dev/null 2>&1
}

check_qdrant_seeded() {
  curl -sf http://localhost:6333/collections/support_kb >/dev/null 2>&1 || return 1
  local pts
  pts=$(curl -sf http://localhost:6333/collections/support_kb | python3 -c "import sys,json; print(json.load(sys.stdin).get('result',{}).get('points_count',0))" 2>/dev/null || echo "0")
  [ "${pts:-0}" -gt 0 ] 2>/dev/null
}

check_webhook() {
  load_env || return 1
  local code
  code=$(curl -s -o /dev/null -w "%{http_code}" \
    -X POST "http://localhost:5678/webhook/support-inquiry" \
    -H "Content-Type: application/json" \
    -H "X-Webhook-Token: ${WEBHOOK_AUTH_TOKEN:-}" \
    -d '{"customer_email":"test@example.com","message":"ping"}' \
    --max-time 10 2>/dev/null || echo "000")
  [ "$code" = "200" ] || [ "$code" = "500" ]
}

check_ollama_models() {
  curl -sf http://localhost:11434/api/tags >/dev/null 2>&1 || return 1
  local tags
  tags=$(curl -sf http://localhost:11434/api/tags | python3 -c "import sys,json; print(' '.join(m.get('name','') for m in json.load(sys.stdin).get('models',[])))" 2>/dev/null || echo "")
  echo "$tags" | grep -q "llama3.2" && echo "$tags" | grep -q "nomic-embed-text"
}

write_env_file() {
  local pg_pass="$1" token="$2" gmail="$3" tz="$4" ollama_host="$5"
  local enc jwt
  enc="$(rand_hex 16)"
  jwt="$(rand_hex 32)"
  cat > "$PROJECT_DIR/.env" <<EOF
# Generated by guide on $(date -u +%Y-%m-%dT%H:%MZ)
POSTGRES_USER=support_agent
POSTGRES_PASSWORD=${pg_pass}
POSTGRES_DB=support_agent

N8N_ENCRYPTION_KEY=${enc}
N8N_USER_MANAGEMENT_JWT_SECRET=${jwt}

OLLAMA_HOST=${ollama_host}
QDRANT_HOST=qdrant
QDRANT_PORT=6333

WEBHOOK_AUTH_TOKEN=${token}
INTENT_CONFIDENCE_THRESHOLD=0.55

GMAIL_FROM_ADDRESS=${gmail}
GENERIC_TIMEZONE=${tz}
EOF
}
